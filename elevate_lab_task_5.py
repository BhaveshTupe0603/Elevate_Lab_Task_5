import pandas as pd
import seaborn as sns
import matplotlib.pyplot as plt

df = pd.read_csv("/content/drive/MyDrive/Dataset/WineQT.csv")

df.head()

df.info()

df.describe()

df.duplicated().sum()

df.isnull().sum()

df['quality'].value_counts()

sns.histplot(df['quality'], bins=6, kde=False, color='mediumpurple')
plt.title('Wine Quality Distribution')
plt.xlabel('Quality')
plt.ylabel('Count')
plt.show()

sns.boxplot(x='quality', y='alcohol', data=df, color='mediumpurple')
plt.title('Alcohol Content vs Quality')
plt.show()

sns.heatmap(df.corr(), annot=True, cmap='coolwarm', fmt=".2f")
plt.title('Correlation Matrix')
plt.show()

sns.pairplot(df[['alcohol', 'sulphates', 'volatile acidity', 'citric acid', 'quality']], hue='quality')
plt.show()

